Wegen des Wartezeitparadoxons.

Wir nehmen an, dass die Ankunftszeiten unabhänging und exponentiell verteilt sind,
wobei Lambda = 0.1 Minuten. Das heißt, dass die erwartete Zeit zwischen zwei Ankünften gleich
E(X) = 1/ λ = 1/0.1= 10 Minuten ist.

Sei X nun die Zufallsvariable, die die Zeit zwischen zwei Ankünften beschreibt.
Es seinen schon s Minuten seit der letzten Ankunft vergangen, was ist die Wahrscheinlichtkeit,
dass wir noch weitere t Minuten warten müssen? Mathematisch:

P(X > s+t | X > s)

Das können wir ausrechnen und es stellt sich heraus, dass die Wahrscheinlichtkeit
gleich ist, ob wir schon gewartet haben oder nicht, daher P(X > t).
